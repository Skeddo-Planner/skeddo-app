try {
  const raw = fs.readFileSync(PROGRAMS_PATH, 'utf-8');
  const existing = JSON.parse(raw);

  const existingIds = new Set(existing.map(p => String(p.id)));
  const toAdd = newPrograms.filter(p => !existingIds.has(p.id));
  const skipped = newPrograms.length - toAdd.length;

  const merged = [...existing, ...toAdd];
  fs.writeFileSync(PROGRAMS_PATH, JSON.stringify(merged, null, 2) + '\n');

  console.log(`Done! Added ${toAdd.length} new programs (${skipped} skipped as duplicates).`);
  console.log(`Total programs now: ${merged.length}`);
} catch (err) {
  console.error('Error:', err.message);
  process.exit(1);
}
