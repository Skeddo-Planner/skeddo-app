// Verify no IDs already exist
const existingIds = new Set(data.map((p) => String(p.id)));
const conflicts = newPrograms.filter((p) => existingIds.has(String(p.id)));
if (conflicts.length > 0) {
  console.error('CONFLICT — IDs already exist:', conflicts.map((p) => p.id).join(', '));
  process.exit(1);
}

// Also verify no registrationUrl duplicates
const existingUrls = new Set(data.map((p) => p.registrationUrl).filter(Boolean));
const urlConflicts = newPrograms.filter((p) => existingUrls.has(p.registrationUrl));
if (urlConflicts.length > 0) {
  console.error('URL CONFLICT:', urlConflicts.map((p) => `${p.id} → ${p.registrationUrl}`).join('\n'));
  process.exit(1);
}

data.push(...newPrograms);

fs.writeFileSync(PROGRAMS_PATH, JSON.stringify(data, null, 2));
